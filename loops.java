public class loops {
    public static void main(String[] args) {
        //for loop
        for(int i = 0; i <= 100; i++){
            System.out.println(i);
        }

        // while loop
        int j = 100;
        while(j >= 0){
            System.out.println(j);
            j--;
        } 
        
        //do while
        int i = 0;
        do {
            System.out.println(i);
            i++;
        }while(i <= 100);
    }
}
